public class Livro {

    String titulo, ISBN, autores, editora;
    int estoque, valor;

    Livro(String titulo, String ISBN, String autores, String editora, int estoque, int valor ){

        this.titulo = titulo;
        this.ISBN = ISBN;
        this.autores = autores;
        this.editora = editora;
        this.estoque = estoque;
        this.valor = valor;

    }


}
